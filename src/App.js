const average = (arr) =>
  arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);

// Components
const Logo = () => {
  return (
    <div className="logo">
      <span role="img">🍿</span>
      <h1>usePopcorn</h1>
    </div>
  );
};

const SearchField = () => {
  const [query, setQuery] = useState('');

  return (
    <input
      className="search"
      type="text"
      placeholder="Search movies..."
      value={query}
      onChange={(e) => setQuery(e.target.value)}
    />
  );
};

const Nav = ({ movies, children }) => {
  return (
    <nav className="nav-bar">
      {children}
      <p className="num-results">
        Found <strong>{movies.length}</strong> results
      </p>
    </nav>
  );
};

const Box = ({ children }) => {
  const [isOpen, setIsOpen] = useState(true);

  return (
    <div className="box">
      <button className="btn-toggle" onClick={() => setIsOpen((open) => !open)}>
        {isOpen ? '–' : '+'}
      </button>
      {isOpen && children}
    </div>
  );
};

const MoviesList = ({ data, forWatched }) => {
  return (
    <ul className="list">
      {data?.map((movie) => (
        <li key={movie.imdbID}>
          <Movie data={movie} forWatched={forWatched} />
        </li>
      ))}
    </ul>
  );
};

const Movie = ({ data, forWatched }) => {
  return (
    <div>
      <img src={data.Poster} alt={`${data.Title} poster`} />
      <div className="inner">
        <h3>{data.Title}</h3>
        {forWatched ? (
          <div>
            <p>
              <span>⭐️</span>
              <span>{data.imdbRating}</span>
            </p>
            <p>
              <span>🌟</span>
              <span>{data.userRating}</span>
            </p>
            <p>
              <span>⏳</span>
              <span>{data.runtime} min</span>
            </p>
          </div>
        ) : (
          <p>
            <span>🗓</span>
            <span>{data.Year}</span>
          </p>
        )}
      </div>
    </div>
  );
};

const WatchedSummary = ({ watched }) => {
  const avgImdbRating = average(watched.map((movie) => movie.imdbRating));
  const avgUserRating = average(watched.map((movie) => movie.userRating));
  const avgRuntime = average(watched.map((movie) => movie.runtime));

  return (
    <div className="summary">
      <h2>Movies you watched</h2>
      <div>
        <p>
          <span>#️⃣</span>
          <span>{watched.length} movies</span>
        </p>
        <p>
          <span>⭐️</span>
          <span>{avgImdbRating}</span>
        </p>
        <p>
          <span>🌟</span>
          <span>{avgUserRating}</span>
        </p>
        <p>
          <span>⏳</span>
          <span>{avgRuntime} min</span>
        </p>
      </div>
    </div>
  );
};

export default function App() {
  const [movies, setMovies] = useState(tempMovieData);
  const [watched, setWatched] = useState(tempWatchedData);

  return (
    <>
      <Nav movies={movies} setMovies={setMovies}>
        <Logo />
        <SearchField />
      </Nav>

      <main className="main">
        <Box>
          <MoviesList data={movies} />
        </Box>
        <Box>
          <WatchedSummary watched={watched} />
          <MoviesList data={watched} forWatched={true} />
        </Box>
      </main>
    </>
  );
}
